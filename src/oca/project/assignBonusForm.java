    private void btnAssignBonusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignBonusActionPerformed
        String jOptionPaneMessage = "";
        if (cboManager.getSelectedItem() instanceof CEO) {
            CEO ceo = (CEO) cboManager.getSelectedItem();
            try {
                jOptionPaneMessage = ceo.assignBonus( (ISubordinate) cboSubordinates.getSelectedItem(), Double.parseDouble(txtBonus.getText()));
            }
            catch(NumberFormatException nfe) {
                jOptionPaneMessage = "Please enter a valid amount.";
                txtBonus.requestFocusInWindow();
                txtBonus.selectAll();
            }
            finally {
                JOptionPane.showMessageDialog(this, jOptionPaneMessage, "Announcement", JOptionPane.INFORMATION_MESSAGE);
                txtBonus.requestFocusInWindow();
                txtBonus.selectAll();
            }
        }
        else {
            Manager manager = (Manager) cboManager.getSelectedItem();
            try {
                jOptionPaneMessage = manager.assignBonus((ISubordinate) cboSubordinates.getSelectedItem(), Double.parseDouble(txtBonus.getText()));
            }
            catch (NumberFormatException nfe) {
                jOptionPaneMessage = "Please enter a valid amount.";
                txtBonus.requestFocusInWindow();
                txtBonus.selectAll();
            }
            finally {
                JOptionPane.showMessageDialog(this, jOptionPaneMessage, "Announcement", JOptionPane.INFORMATION_MESSAGE);
                txtBonus.requestFocusInWindow();
                txtBonus.selectAll();
            }
        }
    }//GEN-LAST:event_btnAssignBonusActionPerformed

    private void btnCheckCurrentBonusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckCurrentBonusActionPerformed
        if((cboSubordinates.getSelectedItem() instanceof ISalariedPerson) == false) {
            JOptionPane.showMessageDialog(this, cboSubordinates.getSelectedItem() + " is a contractor and cannot be allocated a bonus.", "Announcement", JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            ISalariedPerson employee = (ISalariedPerson) cboSubordinates.getSelectedItem();
            JOptionPane.showMessageDialog(this, employee + "'s current bonus: $" + employee.getCurrentBonus(), "Employee Bonus", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnCheckCurrentBonusActionPerformed

