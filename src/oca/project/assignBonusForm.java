    private void cboManagerItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboManagerItemStateChanged
        cboSubordinates.removeAllItems();
        IManager selectedManager = (IManager) cboManager.getSelectedItem();
        ArrayList<ISubordinate> subordinates = selectedManager.getSubordinateList();
                int numOfSalariedSubordinates = 0;
        for (ISubordinate subordinate : subordinates) {
            if(subordinate instanceof ISalariedPerson) {
                numOfSalariedSubordinates++;
            }
        }
        if(numOfSalariedSubordinates == 0) {
            cboSubordinates.addItem("No Salaried Subordinates");
        }
        else {
            for(ISubordinate subordinate : subordinates) {
                cboSubordinates.addItem(subordinate);
            }
        }
    }//GEN-LAST:event_cboManagerItemStateChanged

